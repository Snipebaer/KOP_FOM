package aufgabe1;

public class Buch {
	
	private String titel;
	private String isbn;
	private int anzahlExemplare;
	
	public Buch()
	{
		
	}
	
	public Buch(String titel, String isbn, int anzahlExemplare)
	{
		this.titel = titel;
		this.isbn = isbn;
		this.anzahlExemplare = anzahlExemplare;
	}
	

}
